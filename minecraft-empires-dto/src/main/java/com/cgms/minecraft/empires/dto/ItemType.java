    /**
     * Returns the corresponding Bukkit {@link Material}, or {@code null} if
     * this constant's name does not match any Material registered in this version.
     */
    public Material toMaterial() {
        return Material.getMaterial(name());
    }

    /**
     * Finds the {@link ItemType} whose name matches the given {@link Material},
     * or returns {@code null} if no entry exists.
     */
    public static ItemType fromMaterial(Material material) {
        if (material == null) return null;
        try {
            return valueOf(material.name());
        } catch (IllegalArgumentException e) {
            return null;
        }
    }
